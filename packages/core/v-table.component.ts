@Component({
    selector: "v-table",
    templateUrl: "./v-table.component.html",
    standalone: true,
    imports: [GridComponent, SelectComponent],
    changeDetection: ChangeDetectionStrategy.OnPush,
    providers: [ThyIconRegistry],
})
export class VTableComponent implements OnInit, OnDestroy {
    value = model.required<TableData>();

    options = input.required<any>();

    valueChange = output<TableData>();

    isInitializeSharedType = false;

    sharedType!: SharedType | null;

    provider!: WebsocketProvider;

    constructor(
        private actionManager: ActionManager<any>,
        public cdr: ChangeDetectorRef
    ) {}

    @HostListener("document: keydown", ["$event"])
    onkeyDown(event: KeyboardEvent) {
        if (isHotkey("mod+z", event)) {
            YjsVTable.asUndo(this.value, () => {
                this.actionManager.undoManager.undo();
            });
            return;
        }
        if (isHotkey("mod+shift+z", event)) {
            YjsVTable.asUndo(this.value, () => {
                this.actionManager.undoManager.redo();
            });
            return;
        }
    }

    ngOnInit(): void {
        // this.isInitializeSharedType = !!localStorage.getItem(
        //     "isInitializeSharedType"
        // );
        this.initSharedType();
        this.actionManager.init(this.value);
        this.actionManager.change$.subscribe({
            next: (value) => {
                this.valueChange.emit(value);
                this.cdr.markForCheck();
            },
        });
    }

    initSharedType() {
        this.sharedType = getSharedType(
            this.value(),
            this.isInitializeSharedType
        );
        this.provider = connectProvider(this.sharedType.doc!);
        let isInitialized = false;
        // if (!this.isInitializeSharedType) {
        //     isInitialized = true;
        // }
        this.sharedType.observeDeep((events: any) => {
            if (this.isInitializeSharedType) {
            // //     // console.log(111, this.sharedType!.toJSON());
            // //     // // this.value.set(this.sharedType!.toJSON() as any);
            // //     // this.actionManager.init(this.value);
            // //     // this.actionManager.change$.next(this.value());
            //     isInitialized = true;
            // } else {
                if (!YjsVTable.isLocal(this.value)) {
                    const newProperties = translateEvent(events);
                    if (newProperties?.length) {
                        if (YjsVTable.isUndo(this.value)) {
                            newProperties.forEach((property: any) => {
                                property.forEach((item: any) => {
                                    this.actionManager.execute(item);
                                });
                            });
                        } else {
                            YjsVTable.asRemote(this.value, () => {
                                newProperties.forEach((property: any) => {
                                    property.forEach((item: any) => {
                                        this.actionManager.execute(item);
                                    });
                                });
                            });
                        }
                    }
                }
            }
        });
        this.actionManager.initSharedType(this.sharedType);
        this.isInitializeSharedType = true;
        localStorage.setItem(`isInitializeSharedType`, "true");
    }

    close() {
        this.provider.disconnect();
        this.provider.destroy();
        this.sharedType = null;
    }

    ngOnDestroy(): void {
        this.close();
    }
}

